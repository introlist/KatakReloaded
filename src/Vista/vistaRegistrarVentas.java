    private void EnviarInputs() {
        vendedorMayoreo.setProductosVendidosSeleccionados(getGruposProdActuales());
        
        vendedorMayoreo.setCosto(getCostoTotal());
        
        vendedorMayoreo.guardarVenta();
        
    }

